#include "libft.h"

void    ft_putlnbr(long n)
{
	if (n < -2147483647)
		ft_putstr("-2147483648");
    else
    {
        if (n < 0)
        {
            ft_putchar('-');
            n = -n;
        }
        if (n <= 9)
            ft_putchar(n + 48);
        else
        {
            ft_putlnbr(n / 10);
            ft_putlnbr(n % 10);
        }
    }
}
